public class OperadoresLogicos {
    
    public static void main (String [] args) {
        boolean a = true;
        boolean b = false;

        // Operador && (AND Lógico)
        System.out.println("a && b: " + (a && b));

        // Operador || (OR Lógico) 
        System.out.println("a || b: " + (a || b));

        // Operador ! (NOT Lógico)
        System.out.println("!a: " + (!a));
        System.out.println("!b: " + (!b));

    }
}
